#create a case on what to wear depending on the weather for the day

weather = 'sunny'

case (weather)
when 'rainy'
   puts "Better grab those rain boots"
when 'snowing'
   puts "Break out those mittens and warm clothes"
when 'sunny'
   puts "Put those sunnies on and break out the sun dress"
when 'foggy'
   puts "Better pack an umbrella just in case"
when 'windy'
   puts "Don't worry about fixing that hair today, it will be blowing everywhere"
end
